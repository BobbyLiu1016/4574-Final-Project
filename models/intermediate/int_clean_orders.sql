WITH order_cte AS (
    SELECT *,
        ROW_NUMBER() OVER(PARTITION BY ORDER_ID ORDER BY ORDER_AT_TS) AS ORDER_AT_RANK

    FROM {{ ref('base_webschema_orders')}}
)
SELECT CLIENT_NAME,
    ORDER_AT_TS,
    ORDER_ID,
    PAYMENT_INFO,
    PAYMENT_METHOD,
    PHONE,
    SESSION_ID,
    SHIPPING_ADDRESS,
    SHIPPING_COST,
    CLEAN_SHIPPING_COST,
    STATE,
    TAX_RATE,
    _FIVETRAN_DELETED,
    _FIVETRAN_ID,
    _FIVETRAN_SYNCED_TS
FROM order_cte
WHERE ORDER_AT_RANK = 1
