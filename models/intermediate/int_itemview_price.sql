SELECT 
    DATE(ITEM_VIEW_AT_TS) AS ITEM_VIEW_AT_DATE,
 PRICE_PER_UNIT,
 ITEM_NAME,
 SESSION_ID,
    (ADD_TO_CART_QUANTITY - REMOVE_FROM_CART_QUANTITY) AS NET_QUANTITY, 
    (ADD_TO_CART_QUANTITY - REMOVE_FROM_CART_QUANTITY) * PRICE_PER_UNIT AS TOTAL_PRICE_PER_ITEM  
FROM 
    {{ ref('base_webschema_itemviews') }}
WHERE 
    ADD_TO_CART_QUANTITY - REMOVE_FROM_CART_QUANTITY > 0